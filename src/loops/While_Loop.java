package loops;

public class While_Loop {

	public static void main(String[] args) {
		
		int a=10;//intitalization
		while(a<=100)//condition
		{
			System.out.println(a);
			a=a+10;//updation
		}
		System.out.println(">>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>>");
		int p=21;
		while(p<=210)
		{
			System.out.println(p);
			p=p+21;
		}
	}

}
