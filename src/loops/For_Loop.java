package loops;

public class For_Loop {

	public static void main(String[] args) {
		//i want to print the table of 9
		//intialization; condition; updation
		
		for(int s=9;s<=90;s=s+9) 
		{
			System.out.println(s);
		}
		
		//print table of 23
		System.out.println("--------------------------------------------------------------------------");
		
		for(int a=23;a<=230;a=a+23)
		{
			System.out.println(a);
		}
		{
			System.out.println("------------------------------------------------------------------------");
		}
	//print 100to 1
		for(int z=100;z>=1;z=z-1)
		{
			System.out.println(z);
		}
		
		
	}

}
